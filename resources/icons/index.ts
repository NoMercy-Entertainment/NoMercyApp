// Optimized icon loading with caching
const iconCache = new Map<string, Promise<string>>();

export async function getIcon(name: IconName): Promise<string> {
	if (iconCache.has(name)) {
		return iconCache.get(name)!;
	}

	const loadPromise = loadIconOptimized(name);
	iconCache.set(name, loadPromise);

	return loadPromise;
}

async function loadIconOptimized(name: string): Promise<string> {
	try {
		// First, try to load from the main icons directory
		// @vite-ignore
		const iconModule = await import(`./${name}.svg?import`);

		if (typeof iconModule.default === 'string') {
			return iconModule.default;
		}

		if (typeof iconModule === 'string') {
			return iconModule;
		}

		return iconModule.default || iconModule.src || '';
	}
	catch (error) {
		try {
			// Try to load from extracted mooooom icons
			/* @vite-ignore */
			const moooomModule = await import(`./${name}.svg`);

			if (typeof moooomModule.default === 'string') {
				return moooomModule.default;
			}

			if (typeof moooomModule === 'string') {
				return moooomModule;
			}

			return moooomModule.default || moooomModule.src || '';
		}
		catch (moooomError) {
			try {
				// Fallback to regular import without ?import
				/* @vite-ignore */
				const fallbackModule = await import(`./${name}.svg`);
				return fallbackModule.default || fallbackModule.src || '';
			}
			catch (fallbackError) {
				console.warn(`Failed to load icon: ${name}`, fallbackError);
				return '';
			}
		}
	}
}

// Specific function for mooooom icons to handle type compatibility
import type { MoooomIcons } from './icons';

export async function getMoooomIcon(
	name: keyof typeof MoooomIcons,
): Promise<string> {
	// Use the same cache as the main getIcon function
	const cacheKey = `mooooom-${name}`;
	if (iconCache.has(cacheKey)) {
		return iconCache.get(cacheKey)!;
	}

	const loadPromise = loadIconOptimized(name);
	iconCache.set(cacheKey, loadPromise);

	return loadPromise;
}

